#python operators
###ARITHMETIC OPERATORS###
"""The program show show how to use different operators 
They include:
addition +
subtraction -
multiplication *
division(float) /
division(floor) //
modulus %"""

a = 33
b = 7

#addition  
print(a+b)

#subtraction
print(a-b)

#multiplication
print(a*b)

#division float....gives the answer as a float value
print(a/b)

#division floor....gives the answer as an integer by roundibg down
print(a//b)

#modulus
print(a%b)


###RELATIONAL OPERATORS###
#they compare the values and return true or false
"""They are:
greater than >
less than <
equal to ==
not equal to !=
greater than or equal to >=
less than or equal to <=
"""

x = 12
y = 5
z = 12

#greater than
print(x>y)

#less than
print(x<y)

#equal to
print(x==z)

#not equal to 
print(x!=y)

#greater than or equal to 
print(x>=y)
print(x>=z)

#less than or equal to
print(x<=y)
print(x<=z)




